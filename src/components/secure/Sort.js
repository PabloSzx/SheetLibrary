export function bubbleSort(array) {
    let swapped;
    const a = array.slice(0);
    do {
        swapped = false;
        for (let i = 0; i < a.length - 1; i++) {
            if (a[i] > a[i + 1]) {
                const temp = a[i];
                a[i] = a[i + 1];
                a[i + 1] = temp;
                swapped = true;
            }
        }
    } while (swapped);

    return a;
}
